export type TimelineEventKind =
  | 'unix' // дозиксова эпоха Unix / GNU / BSD
  | 'kernel' // релиз ядра Linux с новой нумерацией
  | 'distro' // появление дистрибутива
  | 'feature' // подсистема / возможность ядра
  | 'project' // отдельный проект (Git, Docker, systemd…)
  | 'milestone' // веха, событие, юридический/индустриальный поворот

export type TimelineEra = {
  /** kebab-id, ключ группировки и якорь на странице */
  id: string
  /** заголовок эпохи */
  label: string
  /** одна строка-аннотация эпохи */
  blurb: string
}

export type TimelineEvent = {
  /** kebab-slug, уникальный, якорь и id для кросс-ссылок */
  id: string
  /** числовой год — первичный ключ сортировки (для диапазона — год начала) */
  year: number
  /** человекочитаемая дата для показа: '1991', 'Август 1991', '2.6.23 · окт 2007' */
  dateLabel: string
  /** id эпохи (TimelineEra.id) */
  era: string
  /** тип события — для цвета/иконки бейджа */
  kind: TimelineEventKind
  /** заголовок события */
  title: string
  /** одна фраза-суть */
  short: string
  /** 1–3 абзаца объяснения, по строке на абзац */
  body: string[]
  /** id модулей, которых событие напрямую касается */
  modules: string[]
  /** id терминов глоссария, которые событие вводит/затрагивает */
  glossary: string[]
  /** id связанных событий таймлайна */
  seeAlso?: string[]
}

