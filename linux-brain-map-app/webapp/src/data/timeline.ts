// Порядок эпох берётся из TIMELINE_ERAS; внутри эпохи события сортируются по году,
// затем по подписи даты (стабильно для нескольких событий одного года).
export const TIMELINE_ERA_IDS: string[] = TIMELINE_ERAS.map((e) => e.id)

export function getEvent(id: string): TimelineEvent | undefined {
  return TIMELINE.find((e) => e.id === id)
}

export function getEra(id: string): TimelineEra | undefined {
  return TIMELINE_ERAS.find((e) => e.id === id)
}

export function getTimelineByEra(): { era: TimelineEra; events: TimelineEvent[] }[] {
  return TIMELINE_ERAS.map((era) => ({
    era,
    events: TIMELINE.filter((e) => e.era === era.id).sort(
      (a, b) => a.year - b.year || a.dateLabel.localeCompare(b.dateLabel, 'ru'),
    ),
  }))
}
